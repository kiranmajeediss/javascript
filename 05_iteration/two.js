/**
 * while: Check FIRST, then run
 * do...while: Run FIRST, then check
 */
//=========while loop===============
let num =10;
while(num<20){
    // console.log(`your number is ${num}`)
    num ++;
}
let index = 0
while (index <= 10) {
//   console.log(`Value of index is ${index}`);
   index = index + 2
 }

//  while in array
let myArray = ["apple","orange","grapes"]
let arr =0;
while(arr < myArray.length){
    // console.log(`Fruite  is ${myArray[arr]}`);
    arr++;
    
}
//=========do while loop===============
let score = 11

do {
    // console.log(`Score is ${score}`);
    score++
} 
while (score <= 10);

